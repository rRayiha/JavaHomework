public class DortIslem {
    public static int Topla(int sayi1, int sayi2){
        return sayi1 + sayi2;
    }

    public static int Cikar(int sayi1, int sayi2){
        return sayi1 - sayi2;
    }

    public static int Carpma(int sayi1, int sayi2){
        return sayi1 * sayi2;
    }

    public static int Bolme(int sayi1, int sayi2){
        return sayi1 / sayi2;
    }
}
